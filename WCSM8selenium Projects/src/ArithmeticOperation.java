
public class ArithmeticOperation {
	
	public static void main(String[] args) {
		
		int num1=20;
		int num2=30;
		
	
			int res=num1+num2;
			System.out.println(res);
			
			int res1=num1-num2;
			System.out.println(res1);
			
			int res2=num1*num2;
			System.out.println(res2);
			
			int res3=num1/num2;
			System.out.println(res3);
			
			int res4=num1%num2;
			System.out.println(res4);
			
	
	}
	}


